<h1>About WordBot</h1>
<p>WordBot is developed by <a href="http://zfast.co.uk" target="_blank">Danomanic</a> & <a href="http://nationpigeon.com" target="_blank">Osaka</a></p>
<p>It was made to archive words from Twitter Statuses.</p>
<h2>Artificial Intelligence</h2>
<p>Like it's developers, this robot has no intelligence. But with your help, you can teach this robot English words, and how to structure words to make a sentence!</p>

<h2>Teaching the Robot</h2>
<p>To teach WordBot, simply tell it if a word is correct/real. For example, click "Yes" if you would use that word in a sentence, or "No" if the word is spelt wrong, or isn't in the English language! </p>


<h2>Removing a word?</h2>
<p>Seen a word that isn't correct? Or is offensive? Or has been falsely added? <br>Email word{at}wordbot.org</p>

